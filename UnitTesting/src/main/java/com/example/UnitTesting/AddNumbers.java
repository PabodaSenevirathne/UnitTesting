package com.example.UnitTesting;

public class AddNumbers {
    public long add(long x, long y) {
        return x + y;
    }

    public long subtract(long x, long y) {
        return x - y;
    }

    public long multiply(long x, long y) {
        return x * y;
    }

    public long divide(long x, long y) {
        return x / y;
    }
}
